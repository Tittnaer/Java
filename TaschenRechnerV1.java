public class TaschenRechnerV1 {
    public static void main(String[] args) {
        int a = 37;
        int b = 7;
        double d = a / b;
        System.out.println("Erste Variable: " + a);
        System.out.println("Zweite Variable: " + b);
        System.out.println(a + "+" + b + "=" + (a + b));
        System.out.println(a + "-" + b + "=" + (a - b));
        System.out.println(a + "*" + b + "=" + a * b);
        System.out.println(a + "/" + b + "=" + d);
        System.out.println(a + "%" + b + "=" + a % b);

    }
}
